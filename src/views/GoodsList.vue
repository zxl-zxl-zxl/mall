<script>
import "../assets/css/base.css";
import "../assets/css/product.css";
import NavHeader from "@/components/NavHeader.vue";
import NavFooter from "@/components/NavFooter.vue";
import NavBread from "@/components/NavBread.vue";
import axios from "axios";
import { currency } from "@/util/currency";
import Modal from "@/components/Modal.vue"; // 模态框
import NavMobile from "@/components/NavMobile.vue";
export default {
  data() {
    return {
      goodsList: [],
      priceFilter: [
        {
          startPrice: "0.00",
          endPrice: "3.00",
        },
        {
          startPrice: "3.00",
          endPrice: "6.00",
        },
        {
          startPrice: "6.00",
          endPrice: "10.00",
        },
        {
          startPrice: "10.00",
          endPrice: "20.00",
        },
      ],
      priceChecked: "all",
      filterBy: false,
      overLayFlag: false,
      sortFlag: true,
      page: 1,
      pageSize: 8,
      busy: true, //由变量busy决定是否执行loadMore，true则不执行loadmore,false则执行loadMore
      loading: false, // 往下滚动"加载图标"的出现效果:默认不出现
      mdShow: false, // 未登录的模态框是否显示
      mdShowCart: false, // 已登录的模态框是否显示
      deviceType: "",//控制底部导航栏显示
      sortAnualFlag:true,//控制默认颜色图标显示
      sortClickFlag:false,//控制有颜色图标显示
    };
  },
  components: {
    NavHeader,
    NavFooter,
    NavBread,
    Modal,
    NavMobile,
  },
  filters: {
    currency: currency,
  },
  mounted: function () {
    this.getGoodsList();
    //根据是否是手机端显示底部导航栏
    if (this._isMobile()) {
      // alert("手机端");
      this.deviceType = "mobile";
    } else {
      // alert("pc端");
      this.deviceType = "pc";
    }
  },
  methods: {
    getGoodsList(flag) {
      var param = {
        page: this.page,
        pageSize: this.pageSize,
        sort: this.sortFlag ? 1 : -1,
        priceLevel: this.priceChecked,
      };
      this.loading = true; // 请求前,往下滚动"加载图标"出现
      //{ params: param }传参
      axios.get("/goods/list", { params: param }).then((result) => {
        let res = result.data;
        console.log(res);
        this.loading = false; // 请求到数据,往下滚动"加载图标"消失
        if (res.status == "0") {
          if (flag) {
            this.goodsList = this.goodsList.concat(res.result.list); //请求的数据拼接
            if (res.result.count == 0) {
              this.busy = true; //禁用,不能再滚动请求了
            } else {
              this.busy = false; //启用,要变成可以滚动请求，下次再滚动还要发请求
            }
          } else {
            this.goodsList = res.result.list; //第一次请求的数据
            this.busy = false;
          }
        } else {
          this.goodsList = [];
        }
      });
    },
    sortGoods() {
      this.sortAnualFlag=false//隐藏默认颜色图标
      this.sortClickFlag=!this.sortClickFlag
      this.sortFlag = !this.sortFlag;
      this.page = 1;
      this.getGoodsList();
    },
    showFilterPop() {
      this.filterBy = true;
      this.overLayFlag = true;
    },
    setPriceFilter(index) {
      this.priceChecked = index;
      this.closePop();
      this.page = 1;
      this.getGoodsList();
    },
    closePop() {
      this.filterBy = false;
      this.overLayFlag = false;
    },
    loadMore: function () {
      this.busy = true; // 禁用,滚动就禁用，防止下一个滚动
      setTimeout(() => {
        this.page++;
        this.getGoodsList(true); // 滚动加载是累加数据，并不是只显示一页数据，so需要传参去请求数据的地方判断一下
      }, 500);
    },
    addCart(productId) {
      // 点击加入购物车
      axios
        .post("/goods/addCart", {
          // 接口设置在server/routes/goods.js
          productId: productId,
        })
        .then((res) => {
          var res = res.data;
          if (res.status == 0) {
            // alert("加入成功");
            this.mdShowCart = true; // 加入购物车成功，成功的模态框显示
            this.$store.commit("updateCartCount", 1);
          } else {
            // alert("msg:" + res.msg);
            this.mdShow = true; // 未登录模态框显示
          }
        });
    },
    closeModal() {
      // 关闭模态框
      this.mdShow = false; // 未登录模态框消失
      this.mdShowCart = false; // 未登录模态框消失
    },
    _isMobile() {
      let flag = navigator.userAgent.match(
        /(phone|pad|pod|iPhone|iPod|ios|iPad|Android|Mobile|BlackBerry|IEMobile|MQQBrowser|JUC|Fennec|wOSBrowser|BrowserNG|WebOS|Symbian|Windows Phone)/i
      );
      return flag;
    },
  },
};
</script>

